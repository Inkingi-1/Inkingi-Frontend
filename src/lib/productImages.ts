/** Match longest / most specific patterns first */
const IMAGE_BY_KEYWORD: [string, string][] = [
  ["cimerwa", MATERIAL_IMAGES.cement],
  ["cement|concrete|mortar|portland", MATERIAL_IMAGES.cement],
  ["hammer drill|drill|power-tool|power tool", MATERIAL_IMAGES.drill],
  ["steel|rebar|reinforcement|tmt|rod", MATERIAL_IMAGES.steel],
  ["pvc|plumb|pipe|hdpe", MATERIAL_IMAGES.pvc],
  ["paint|finishes|weatherguard|coating|varnish", MATERIAL_IMAGES.paint],
  ["roof|ibr|sheet|corrugated", MATERIAL_IMAGES.roofing],
  ["electric|wire|cable|breaker|conduit", MATERIAL_IMAGES.electrical],
  ["tool|hammer|hardware|wrench", MATERIAL_IMAGES.tools],
  ["paver|block|brick|tile", MATERIAL_IMAGES.paint],
  ["hardware|quincaillerie|store|supplier|vendor", MATERIAL_IMAGES.storeBanner],
];

export function resolveMaterialImage(...hints: (string | undefined)[]): string {
  const text = hints.filter(Boolean).join(" ").toLowerCase();
  for (const [pattern, url] of IMAGE_BY_KEYWORD) {
    if (pattern.split("|").some((kw) => text.includes(kw))) return url;
  }
  return MATERIAL_IMAGES.default;
}

export function resolveProductImage(
  name?: string,
  tags?: string[],
  category?: string
): string {
  return resolveMaterialImage(name, category, ...(tags ?? []));
}

export function resolveStoreImage(_storeName?: string): string {
  return MATERIAL_IMAGES.storeBanner;
}

export function resolveStoreLogo(_storeName?: string): string {
  return MATERIAL_IMAGES.storeLogo;
}

export function pickProductImage(
  _images: string[] | undefined,
  name?: string,
  tags?: string[],
  category?: string
): string {
  return resolveProductImage(name, tags, category);
}

export function pickVendorBanner(vendor: { banner?: string; logo?: string; storeName?: string }): string {
  const banner = vendor.banner;
  const logo = vendor.logo;
  if (banner && !banner.includes("placehold.co") && !banner.includes("unsplash.com")) return banner;
  if (logo && !logo.includes("placehold.co") && !logo.includes("unsplash.com")) return logo;
  return resolveStoreImage(vendor.storeName);
}

export function pickVendorLogo(vendor: { logo?: string; storeName?: string }): string {
  const logo = vendor.logo;
  if (logo && !logo.includes("placehold.co") && !logo.includes("unsplash.com")) return logo;
  return resolveStoreLogo(vendor.storeName);
}
